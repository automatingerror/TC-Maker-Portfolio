<h1>TC Maker Portfolio Project</h1>
<p>This simple webapp is going to take input of project information and spit out formatted svg images (or pdf documents)
that can be printed and bound into a portfolio of activity within TC Maker.</p>

<p>Check back frequently for updates.</p>

<p>1. Information to gather:</p>
<dl>
	<dt>Project Title</dt>
	<dd>Title of project. Try to be somewhat descriptive</dd>
	<dt>Maker Name(s)</dt>
		<dd>Please enter names of makers who developed this project.</dd>
	<dt>Maker Biography</dt>
	<dt>Completion Date</dt>
	<dt>Materials Used</dt>
	<dt>Process/Function</dt>
	<dt>Key Project Photo</dt>
	<dt>Accent Photo #1</dt>
	<dt>Accent Photo #2</dt>
	<dt>Accent Photo #3</dt>	
</dl>
